// Package version содержит метаданные сборки юзербота.
// Назначение:
//   - Name — человекочитаемое имя приложения для логов/CLI;
//   - Version — строка версии (SemVer или тег) для печати при старте и в CLI.
//
// Переменные оставлены изменяемыми (var, не const), чтобы их можно было переопределять
// при сборке с помощью флага:
//
//	go build -ldflags "-X '…/internal/support/version.Name=telegram-userbot' -X '…/internal/support/version.Version=v1.2.3'"
//
// Пакет не тянет зависимостей и безопасен для использования в ранней инициализации.
package version

var (
	// Name — отображаемое имя приложения. Используется в логах, баннерах CLI и сообщениях.
	// Может быть переопределено через -ldflags при сборке.
	Name = "telegram-userbot"
	// Version — строка версии сборки. Ожидается SemVer вида vMAJOR.MINOR.PATCH либо произвольный тег.
	// Переопределяется при сборке через -ldflags. Пустая строка может трактоваться как "dev" билды.
	Version = "v0.8.9"
)
